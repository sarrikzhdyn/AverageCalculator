using System; 

namespace FizzBuzz 
{
    class Program 
    {
        static void Main(string[] args) // Точка входа в программу
        {
            // Выводим сообщение о назначении программы
            Console.WriteLine("Программа выводит числа от 1 до 100, заменяя числа, делящиеся на 3, на 'Fizz', на 5 — на 'Buzz', на оба — на 'FizzBuzz'.");

            // Цикл от 1 до 100
            for (int i = 1; i <= 100; i++)
            {
                // Проверяем, делится ли число одновременно на 3 и на 5 (на 15)
                if (i % 3 == 0 && i % 5 == 0)
                {
                    Console.WriteLine("FizzBuzz"); // Выводим 'FizzBuzz' для чисел, делящихся на 15
                }
                // Проверяем, делится ли число только на 3
                else if (i % 3 == 0)
                {
                    Console.WriteLine("Fizz"); // Выводим 'Fizz' для чисел, делящихся на 3
                }
                // Проверяем, делится ли число только на 5
                else if (i % 5 == 0)
                {
                    Console.WriteLine("Buzz"); // Выводим 'Buzz' для чисел, делящихся на 5
                }
                // Если число не делится ни на 3, ни на 5, выводим само число
                else
                {
                    Console.WriteLine(i); // Выводим число
                }
            }
        }
    }
}